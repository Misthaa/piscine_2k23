#include <unistd.h>

void	numbers(int d, int u)
{
	write(1, &"0123456789"[u / 10], 1);
	write(1, &"0123456789"[u % 10], 1);
	write(1, " ", 1);
	write(1, &"0123456789"[d / 10], 1);
	write(1, &"0123456789"[d % 10], 1);
	if (u != 98)
		write(1, ", ", 2);
}

void	ft_print_comb2(void)
{
	int	d;
	int	u;

	u = -1;
	while (++u <= 98)
	{
		d = u + 1;
		while (d <= 99)
		{
			numbers(d, u);
			d++;
		}
	}
}
