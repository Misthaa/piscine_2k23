int	verif_map(char **map, int par[4])
{
	int	x;
	int	y;

	x = 0;
	while (map[x])
	{
		y = 0;
		while (map[x][y])
		{
			if (map[x][y] != par[1] && map[x][y] != par[2])
				return (-1);
			y++;
		}
		x++;
	}
	return (0);
}
